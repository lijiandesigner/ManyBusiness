package com.ssm.dto;

public class Brand {
	private Integer id;
	private String brand_name;
	private String brand_pic;
	private Integer brand_num;
	private String brand_goods;
	private Integer brand_zh;
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getBrand_name() {
		return brand_name;
	}
	public void setBrand_name(String brand_name) {
		this.brand_name = brand_name;
	}
	public String getBrand_pic() {
		return brand_pic;
	}
	public void setBrand_pic(String brand_pic) {
		this.brand_pic = brand_pic;
	}
	public Integer getBrand_num() {
		return brand_num;
	}
	public void setBrand_num(Integer brand_num) {
		this.brand_num = brand_num;
	}
	public String getBrand_goods() {
		return brand_goods;
	}
	public void setBrand_goods(String brand_goods) {
		this.brand_goods = brand_goods;
	}
	public Integer getBrand_zh() {
		return brand_zh;
	}
	public void setBrand_zh(Integer brand_zh) {
		this.brand_zh = brand_zh;
	}
	

}
